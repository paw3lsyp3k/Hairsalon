function show1() {
    var x = 0;
    for (i = 1; i < 7; i++) {
      x++;
      document.getElementById("covidanswer" + x).style.display = "none";
      document.getElementById("covidicon" + x).style.display = "block";
      document.getElementById("covidicon" + x + x).style.display = "none";
    }
    document.getElementById("covidanswer1").style.display = "block";
    document.getElementById("covidicon1").style.display = "none";
    document.getElementById("covidicon11").style.display = "block";
  }
  
